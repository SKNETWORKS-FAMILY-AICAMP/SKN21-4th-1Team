def is_labor_law(law_name: str) -> bool:
    """
    법령명이 노동법에 해당하는지 확인
    
    Args:
        law_name: 확인할 법령명
        
    Returns:
        True if 노동법, False otherwise
    """
    if not law_name:
        return False
    
    # 정확한 매칭
    if law_name in LABOR_LAWS:
        return True
    
    # 부분 매칭 (예: "근로기준법 제50조" → "근로기준법")
    for labor_law in LABOR_LAWS:
        if labor_law in law_name or law_name in labor_law:
            return True
    
    # 키워드 매칭
    for keyword in LABOR_LAW_KEYWORDS:
        if keyword in law_name:
            return True
    
    return False


def get_labor_law_category(law_name: str) -> str:
    """
    법령명을 기반으로 카테고리 반환
    
    Args:
        law_name: 확인할 법령명
        
    Returns:
        "노동법" or "기타"
    """
    return "노동법" if is_labor_law(law_name) else "기타"
